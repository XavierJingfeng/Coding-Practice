void dfs(int x, int y, vector<vector<string>>& grid){
	int dx[4] = {0,1,0,-1};
	    int dy[4] = {1,0,-1,0};
	int m = grid.size();
    
    int n = grid[0].size();
    if(grid[x][y] == "1"){
        grid[x][y] = "0";
        for(int i = 0; i<4; ++i){
            int xx = x + dx[i];
            int yy = y + dy[i];
            if( xx>=0 && xx<m && yy>=0 && yy<n && grid[xx][yy] == "1"){
                dfs(xx, yy, grid);
            }
        }
    }
}

int numIslands(vector<vector<string>>& grid) {
	
    int m = grid.size();
    if(m == 0) return 0;
    int n = grid[0].size();
    int nums = 0;
    for(int i = 0; i<m; ++i){
        for(int j = 0; j<n; ++j){
            if(grid[i][j] == "1"){
                dfs(i, j, grid);
                nums++;
            }                
        }
    }
    return nums;
}

